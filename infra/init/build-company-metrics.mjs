#!/usr/bin/env node
/**
 * Backfill company_metrics from a SQLite/D1 corpus.
 *
 * Usage:
 *   pnpm exec tsx infra/init/build-company-metrics.mjs [db-path] [output.sql] [--limit N]
 *   LIMIT=10 pnpm exec tsx infra/init/build-company-metrics.mjs
 *
 * Defaults:
 *   db-path: infra/sampledb/d1_remote_corpus_merged.db
 *   output:  infra/init/company_metrics.sql
 *   limit:   all sec_codes (use --limit or LIMIT for small-sample testing)
 */
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import Database from "better-sqlite3";
import {
  metricsFromPeriods,
  metricsToDenormalizedColumns,
} from "../../packages/metrics/src/index.ts";

function parseCliArgs(argv) {
  const positional = [];
  let limit = process.env.LIMIT ? Number.parseInt(process.env.LIMIT, 10) : null;

  for (let i = 0; i < argv.length; i++) {
    if (argv[i] === "--limit") {
      limit = Number.parseInt(argv[i + 1] ?? "", 10);
      i++;
      continue;
    }
    if (argv[i] !== "--") {
      positional.push(argv[i]);
    }
  }

  if (limit != null && (Number.isNaN(limit) || limit < 1)) {
    console.error("Invalid --limit / LIMIT (must be a positive integer)");
    process.exit(1);
  }

  return { positional, limit };
}

const root = join(dirname(fileURLToPath(import.meta.url)), "../..");
const { positional, limit } = parseCliArgs(process.argv.slice(2));
const dbPath = positional[0] ?? join(root, "infra/sampledb/d1_remote_corpus_merged.db");
const outPath = positional[1] ?? join(root, "infra/init/company_metrics.sql");

function escSql(s) {
  return String(s).replace(/'/g, "''");
}

function blockToRecord(json) {
  if (!json) return {};
  try {
    const obj = JSON.parse(json);
    if (!obj || typeof obj !== "object") return {};
    const out = {};
    for (const [k, v] of Object.entries(obj)) {
      if (v == null) continue;
      out[k] = String(v);
    }
    return out;
  } catch {
    return {};
  }
}

const db = new Database(dbPath, { readonly: true });

const latestRowsAll = db
  .prepare(
    `SELECT sec_code, edinet_code, filer_name
     FROM sec_code_latest_periods
     WHERE sec_code IS NOT NULL AND sec_code != ''
     ORDER BY sec_code`,
  )
  .all();

const latestRows = limit ? latestRowsAll.slice(0, limit) : latestRowsAll;
if (limit) {
  console.log(`Processing first ${limit} of ${latestRowsAll.length} sec_codes`);
}

const periodsStmt = db.prepare(
  `SELECT doc_id, doc_type, period_start, period_end, submit_date_time,
          filer_name, summary_json, pl_json, bs_json, cf_json
   FROM period_financials
   WHERE sec_code = ?
   ORDER BY period_end DESC, submit_date_time DESC
   LIMIT 6`,
);

const docDescStmt = db.prepare(`SELECT doc_description FROM documents WHERE doc_id = ?`);

const lines = [
  "-- Generated by infra/init/build-company-metrics.mjs — do not edit by hand.",
  "PRAGMA foreign_keys = ON;",
  "",
];

let written = 0;
const now = new Date().toISOString();

for (const row of latestRows) {
  const periodsRaw = periodsStmt.all(row.sec_code);
  if (periodsRaw.length === 0) continue;

  const periods = periodsRaw
    .map((p) => {
      const doc = docDescStmt.get(p.doc_id);
      let docDescription = doc?.doc_description ?? "";
      if (!docDescription) {
        if (p.doc_type === "annual") docDescription = "有価証券報告書";
        else if (p.doc_type?.includes("quarter")) docDescription = "四半期報告書";
        else if (p.doc_type?.includes("semi")) docDescription = "半期報告書";
      }
      return {
        periodStart: p.period_start ?? "",
        periodEnd: p.period_end,
        docID: p.doc_id,
        docDescription,
        submitDateTime: p.submit_date_time ?? "",
        summary: blockToRecord(p.summary_json),
        pl: blockToRecord(p.pl_json),
        bs: blockToRecord(p.bs_json),
        cf: blockToRecord(p.cf_json),
      };
    })
    .reverse();

  const company = {
    edinetCode: row.edinet_code,
    secCode: row.sec_code,
    filerName: row.filer_name,
    periods,
  };

  const metrics = metricsFromPeriods(company);
  if (!metrics) continue;

  const denorm = metricsToDenormalizedColumns(metrics);
  const metricsJson = escSql(JSON.stringify(metrics));

  lines.push(`INSERT OR REPLACE INTO company_metrics (
  sec_code, edinet_code, filer_name, calc_date, fiscal_month,
  metrics_json, sales, roe, equity_ratio, total_assets, updated_at
) VALUES (
  '${escSql(row.sec_code)}',
  '${escSql(row.edinet_code)}',
  '${escSql(row.filer_name)}',
  ${metrics.calcDate ? `'${escSql(metrics.calcDate)}'` : "NULL"},
  ${metrics.fiscalMonth ? `'${escSql(metrics.fiscalMonth)}'` : "NULL"},
  '${metricsJson}',
  ${denorm.sales ?? "NULL"},
  ${denorm.roe ?? "NULL"},
  ${denorm.equityRatio ?? "NULL"},
  ${denorm.totalAssets ?? "NULL"},
  '${escSql(now)}'
);`);
  lines.push("");
  written++;
}

db.close();

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, `${lines.join("\n")}\n`);
console.log(
  `Wrote ${outPath} (${written} rows from ${latestRows.length} sec codes` +
    (limit ? `, limit=${limit}` : "") +
    ")",
);
