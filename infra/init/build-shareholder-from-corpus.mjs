#!/usr/bin/env node
/**
 * Backfill shareholder_snapshots from a local corpus SQLite DB.
 *
 * Reads period_financials.raw_tsv_path and re-parses major shareholders from
 * local TSV files when they exist alongside the corpus.
 *
 * D1-only exports often store CI-local paths (e.g. data/raw/...) without the
 * actual TSV bytes — those rows are skipped. Full backfill requires either:
 *   - re-ingest via apps/wrapper (shareholders are written during ingest), or
 *   - a local corpus DB that still has TSV files at raw_tsv_path.
 *
 * Usage:
 *   pnpm exec tsx infra/init/build-shareholder-from-corpus.mjs [db-path] [output.sql] [--limit N]
 *   LIMIT=10 pnpm exec tsx infra/init/build-shareholder-from-corpus.mjs
 *
 * Defaults:
 *   db-path: infra/sampledb/d1_remote_corpus_merged.db
 *   output:  infra/init/shareholder_snapshots_from_corpus.sql
 */
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, isAbsolute, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import Database from "better-sqlite3";
import {
  majorShareholdersToApiEntries,
  parseMajorShareholdersFromRaw,
} from "../../packages/metrics/src/index.ts";

function parseCliArgs(argv) {
  const positional = [];
  let limit = process.env.LIMIT ? Number.parseInt(process.env.LIMIT, 10) : null;

  for (let i = 0; i < argv.length; i++) {
    if (argv[i] === "--limit") {
      limit = Number.parseInt(argv[i + 1] ?? "", 10);
      i++;
      continue;
    }
    if (argv[i] !== "--") {
      positional.push(argv[i]);
    }
  }

  if (limit != null && (Number.isNaN(limit) || limit < 1)) {
    console.error("Invalid --limit / LIMIT (must be a positive integer)");
    process.exit(1);
  }

  return { positional, limit };
}

const root = join(dirname(fileURLToPath(import.meta.url)), "../..");
const { positional, limit } = parseCliArgs(process.argv.slice(2));
const dbPath = positional[0] ?? join(root, "infra/sampledb/d1_remote_corpus_merged.db");
const outPath = positional[1] ?? join(root, "infra/init/shareholder_snapshots_from_corpus.sql");

function escSql(s) {
  return String(s).replace(/'/g, "''");
}

function resolveTsvPath(rawPath, dbDir) {
  if (!rawPath) return null;
  const candidates = [
    rawPath,
    resolve(dbDir, rawPath),
    resolve(root, rawPath),
  ];
  if (isAbsolute(rawPath)) {
    candidates.unshift(rawPath);
  }
  for (const candidate of candidates) {
    if (existsSync(candidate)) return candidate;
  }
  return null;
}

function readTsvRows(filePath) {
  const buf = readFileSync(filePath);
  const text = buf.toString("utf16le");
  const lines = text.split(/\r?\n/).filter((line) => line.trim());
  if (lines.length === 0) return [];
  const start = lines[0].includes("要素ID") ? 1 : 0;
  return lines.slice(start).map((line) => line.split("\t"));
}

const db = new Database(dbPath, { readonly: true });
const dbDir = dirname(resolve(dbPath));

const rowsAll = db
  .prepare(
    `SELECT sec_code, period_end, doc_id, doc_type, raw_tsv_path
     FROM period_financials
     WHERE sec_code IS NOT NULL
       AND sec_code != ''
       AND doc_type IN ('annual', 'semiannual')
     ORDER BY sec_code, period_end`,
  )
  .all();

const rows = limit ? rowsAll.slice(0, limit) : rowsAll;
if (limit) {
  console.log(`Processing first ${limit} of ${rowsAll.length} period_financials rows`);
}

const lines = [
  "-- Generated by infra/init/build-shareholder-from-corpus.mjs — do not edit by hand.",
  "-- Skipped rows (missing TSV) are logged to stderr; re-ingest for full D1 backfill.",
  "PRAGMA foreign_keys = ON;",
  "",
];

const now = new Date().toISOString();
let written = 0;
let skippedMissingTsv = 0;
let skippedEmpty = 0;

for (const row of rows) {
  const tsvPath = resolveTsvPath(row.raw_tsv_path, dbDir);
  if (!tsvPath) {
    skippedMissingTsv++;
    continue;
  }

  const parsed = parseMajorShareholdersFromRaw({ rows: readTsvRows(tsvPath) });
  const entries = majorShareholdersToApiEntries(parsed);
  if (!entries.length) {
    skippedEmpty++;
    continue;
  }

  const entriesJson = escSql(JSON.stringify(entries));
  lines.push(`INSERT OR REPLACE INTO shareholder_snapshots (
  sec_code, period_end, doc_id, entries_json, updated_at
) VALUES (
  '${escSql(row.sec_code)}',
  '${escSql(row.period_end)}',
  ${row.doc_id ? `'${escSql(row.doc_id)}'` : "NULL"},
  '${entriesJson}',
  '${escSql(now)}'
);`);
  lines.push("");
  written++;
}

db.close();

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, `${lines.join("\n")}\n`);

console.log(
  `Wrote ${outPath} (${written} snapshots from ${rows.length} rows` +
    (limit ? `, limit=${limit}` : "") +
    ")",
);
if (skippedMissingTsv > 0) {
  console.warn(
    `Skipped ${skippedMissingTsv} rows: raw_tsv_path not found on disk. ` +
      "D1-only exports need re-ingest or a local corpus with TSV files.",
  );
}
if (skippedEmpty > 0) {
  console.warn(`Skipped ${skippedEmpty} rows: no major shareholder entries in TSV.`);
}
