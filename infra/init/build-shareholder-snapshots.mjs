#!/usr/bin/env node
/**
 * Backfill shareholder_snapshots from sample JSON files.
 *
 * Usage:
 *   pnpm exec tsx infra/init/build-shareholder-snapshots.mjs [input-dir] [output.sql] [--limit N]
 *   LIMIT=5 pnpm exec tsx infra/init/build-shareholder-snapshots.mjs
 *
 * input-dir: directory with {secCode}.json files (API snapshots format)
 * Default input: infra/init/sample/shareholders
 * limit: process only first N JSON files (for small-sample testing)
 */
import { existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

function parseCliArgs(argv) {
  const positional = [];
  let limit = process.env.LIMIT ? Number.parseInt(process.env.LIMIT, 10) : null;

  for (let i = 0; i < argv.length; i++) {
    if (argv[i] === "--limit") {
      limit = Number.parseInt(argv[i + 1] ?? "", 10);
      i++;
      continue;
    }
    if (argv[i] !== "--") {
      positional.push(argv[i]);
    }
  }

  if (limit != null && (Number.isNaN(limit) || limit < 1)) {
    console.error("Invalid --limit / LIMIT (must be a positive integer)");
    process.exit(1);
  }

  return { positional, limit };
}

const root = join(dirname(fileURLToPath(import.meta.url)), "../..");
const { positional, limit } = parseCliArgs(process.argv.slice(2));
const inputDir = positional[0] ?? join(root, "infra/init/sample/shareholders");
const outPath = positional[1] ?? join(root, "infra/init/shareholder_snapshots.sql");

function escSql(s) {
  return String(s).replace(/'/g, "''");
}

function parseShareholderFile(raw) {
  const snapshots = [];
  if (!Array.isArray(raw.snapshots)) {
    return snapshots;
  }
  for (const s of raw.snapshots) {
    snapshots.push({
      periodEnd: s.periodEnd,
      docId: s.docId ?? null,
      entries: s.entries ?? [],
    });
  }
  return snapshots;
}

const lines = [
  "-- Generated by infra/init/build-shareholder-snapshots.mjs — do not edit by hand.",
  "PRAGMA foreign_keys = ON;",
  "",
];

if (!existsSync(inputDir)) {
  console.error(`Input directory not found: ${inputDir}`);
  process.exit(1);
}

const now = new Date().toISOString();
let written = 0;

const jsonFiles = readdirSync(inputDir)
  .filter((f) => f.endsWith(".json"))
  .sort();
const filesToProcess = limit ? jsonFiles.slice(0, limit) : jsonFiles;
if (limit) {
  console.log(`Processing first ${limit} of ${jsonFiles.length} JSON files`);
}

for (const file of filesToProcess) {
  const secCode = file.replace(/\.json$/, "");
  const raw = JSON.parse(readFileSync(join(inputDir, file), "utf8"));
  const snapshots = parseShareholderFile(raw);
  for (const snap of snapshots) {
    if (!snap.periodEnd || !snap.entries?.length) continue;
    const entriesJson = escSql(JSON.stringify(snap.entries));
    lines.push(`INSERT OR REPLACE INTO shareholder_snapshots (
  sec_code, period_end, doc_id, entries_json, updated_at
) VALUES (
  '${escSql(secCode)}',
  '${escSql(snap.periodEnd)}',
  ${snap.docId ? `'${escSql(snap.docId)}'` : "NULL"},
  '${entriesJson}',
  '${escSql(now)}'
);`);
    lines.push("");
    written++;
  }
}

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, `${lines.join("\n")}\n`);
console.log(
  `Wrote ${outPath} (${written} snapshots from ${filesToProcess.length} files${limit ? `, limit=${limit}` : ""} in ${inputDir})`,
);
