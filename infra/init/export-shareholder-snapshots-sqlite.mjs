#!/usr/bin/env node
/**
 * Export all shareholder_snapshots rows from a SQLite DB to D1-ready SQL.
 *
 * Usage:
 *   pnpm exec tsx infra/init/export-shareholder-snapshots-sqlite.mjs [db-path] [output.sql]
 */
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import Database from "better-sqlite3";

const root = join(dirname(fileURLToPath(import.meta.url)), "../..");
const dbPath = process.argv[2] ?? join(root, "apps/wrapper/data/edinet.db");
const outPath = process.argv[3] ?? join(root, "infra/init/shareholder_snapshots_export.sql");

function escSql(s) {
  return String(s).replace(/'/g, "''");
}

const db = new Database(dbPath, { readonly: true });
const rows = db
  .prepare(
    `SELECT sec_code, period_end, doc_id, entries_json, updated_at
     FROM shareholder_snapshots
     ORDER BY sec_code, period_end`,
  )
  .all();
db.close();

const lines = [
  "-- Generated by infra/init/export-shareholder-snapshots-sqlite.mjs — do not edit by hand.",
  "PRAGMA foreign_keys = ON;",
  "",
];

for (const row of rows) {
  lines.push(`INSERT OR REPLACE INTO shareholder_snapshots (
  sec_code, period_end, doc_id, entries_json, updated_at
) VALUES (
  '${escSql(row.sec_code)}',
  '${escSql(row.period_end)}',
  ${row.doc_id ? `'${escSql(row.doc_id)}'` : "NULL"},
  '${escSql(row.entries_json)}',
  '${escSql(row.updated_at ?? new Date().toISOString())}'
);`);
  lines.push("");
}

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, `${lines.join("\n")}\n`);
console.log(`Wrote ${outPath} (${rows.length} snapshots from ${dbPath})`);
