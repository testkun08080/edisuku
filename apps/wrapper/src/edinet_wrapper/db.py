"""SQLite UPSERT helpers shared by ingest_daily / backfill / publish_to_d1.

Schema is generated by drizzle-kit from packages/db/src/schema.ts; the
SQL lives at packages/db/migrations/0000_init.sql.
"""

from __future__ import annotations

import json
import sqlite3
from collections.abc import Iterable
from pathlib import Path
from typing import Any

REPO_ROOT = Path(__file__).resolve().parent.parent.parent.parent.parent
MIGRATIONS_DIR = REPO_ROOT / "packages" / "db" / "migrations"
SCHEMA_PATH = MIGRATIONS_DIR / "0000_init.sql"
SCHEMA_PATH_0001 = MIGRATIONS_DIR / "0001_company_metrics.sql"
SCHEMA_PATH_0002 = MIGRATIONS_DIR / "0002_drop_legacy_tables.sql"


def _table_exists(conn: sqlite3.Connection, name: str) -> bool:
    row = conn.execute(
        "SELECT 1 FROM sqlite_master WHERE type='table' AND name=? LIMIT 1",
        (name,),
    ).fetchone()
    return row is not None


def open_db(path: Path) -> sqlite3.Connection:
    path.parent.mkdir(parents=True, exist_ok=True)
    conn = sqlite3.connect(str(path))
    conn.execute("PRAGMA foreign_keys = ON")
    conn.execute("PRAGMA journal_mode = WAL")
    conn.row_factory = sqlite3.Row
    return conn


def _run_migration_sql(conn: sqlite3.Connection, schema_path: Path) -> None:
    if not schema_path.exists():
        raise FileNotFoundError(f"schema not found: {schema_path}")
    sql = schema_path.read_text(encoding="utf-8").replace("--> statement-breakpoint", "")
    conn.executescript(sql)


def apply_schema(
    conn: sqlite3.Connection,
    schema_paths: Iterable[Path] | None = None,
) -> None:
    if schema_paths is not None:
        for schema_path in schema_paths:
            _run_migration_sql(conn, schema_path)
        conn.commit()
        return

    if not _table_exists(conn, "companies"):
        _run_migration_sql(conn, SCHEMA_PATH)
    if not _table_exists(conn, "company_metrics"):
        _run_migration_sql(conn, SCHEMA_PATH_0001)
    _run_migration_sql(conn, SCHEMA_PATH_0002)
    conn.commit()


def upsert_company(
    conn: sqlite3.Connection,
    *,
    edinet_code: str,
    sec_code: str | None,
    filer_name: str,
    listed_category: str | None = None,
    industry: str | None = None,
) -> None:
    conn.execute(
        """
        INSERT INTO companies (edinet_code, sec_code, filer_name, listed_category, industry)
        VALUES (?, ?, ?, ?, ?)
        ON CONFLICT(edinet_code) DO UPDATE SET
          sec_code=excluded.sec_code,
          filer_name=excluded.filer_name,
          listed_category=excluded.listed_category,
          industry=excluded.industry,
          updated_at=CURRENT_TIMESTAMP
        """,
        (edinet_code, sec_code, filer_name, listed_category, industry),
    )


def upsert_document(conn: sqlite3.Connection, doc: dict[str, Any]) -> None:
    conn.execute(
        """
        INSERT INTO documents (
          doc_id, edinet_code, sec_code, doc_type, ordinance_code, form_code,
          doc_type_code, period_start, period_end, submit_date_time,
          withdrawal_status, doc_description, source_meta_json
        )
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        ON CONFLICT(doc_id) DO UPDATE SET
          sec_code=excluded.sec_code,
          doc_type=excluded.doc_type,
          period_start=excluded.period_start,
          period_end=excluded.period_end,
          submit_date_time=excluded.submit_date_time,
          withdrawal_status=excluded.withdrawal_status,
          doc_description=excluded.doc_description,
          source_meta_json=excluded.source_meta_json,
          updated_at=CURRENT_TIMESTAMP
        """,
        (
            doc["doc_id"],
            doc["edinet_code"],
            doc.get("sec_code"),
            doc["doc_type"],
            doc.get("ordinance_code"),
            doc.get("form_code"),
            doc.get("doc_type_code"),
            doc.get("period_start"),
            doc.get("period_end"),
            doc.get("submit_date_time"),
            doc.get("withdrawal_status"),
            doc.get("doc_description"),
            json.dumps(doc.get("source_meta", {}), ensure_ascii=False),
        ),
    )


